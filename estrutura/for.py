# O for é um laço que é executado um número determinado de vezes

for variavel in range(0, 10): # Definimos um variável que armazena cada iteração e por meio da função range definimos o valor inicial/final/intervalo de valores
    print(variavel) # O valor final não é incluido na iteração

for x in [1,2,3,4,5,6]: # Também podemos usar um array para definir os valores
    print(x)