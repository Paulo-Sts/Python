# Por meio da função interna bool() podemos definir um valor que retorna um booleano

print(bool("")) # Espaço vazio retorna False
print(bool(0)) # O valor 0 retorna False
print(bool(1)) # Outros valores retornam True
print(bool(-20))
print(bool("Oi"))